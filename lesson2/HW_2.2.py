string = input('')
print('\n')
print(string[2], ': вывод третьего элемента\n')
print(string[-2], ': вывод предпоследнего элемента\n')
print(string[0:5:1], ': вывод первых пяти символов\n')
print(string[:-2:], ': вывод всей строки, кроме последних двух символов\n')
print(string[1::2], ': вывод всех чётных символов (индекс начинается с 0, поэтому символы выводятся, начиная с первого)\n')
print(string[3::2], ': вывод всех символов с нечётными индексами, начиная со второго символа строки\n')
print(string[::-1], ': вывод всех символов в обратном порядке\n')
print(string[::-2], ': вывод всех символов строки через один в обратном порядке, начиная с последнего\n')
print(len(string), ': вывод длины данной строки\n')
